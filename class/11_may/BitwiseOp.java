class     BitwiseOp{

	public static void main (String args[]){


	//bitwise not operator(~) or tild sign or negoation operator
	int x=~9;
	System.out.println("bitwise : "+x);
	
	//bitwise left shift operator

	int y=4;
	int z=y<<2;
	System.out.println("bitwise left shift : "+z);
	System.out.println(z<<3);

	//bitwise right shift operator
	int a=4;
	int c=a>>1;
	System.out.println("bitwise left shift : "+c);
	System.out.println(c>>1);

}
}