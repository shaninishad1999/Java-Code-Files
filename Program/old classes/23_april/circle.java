public class circle {
    public static void main(String args[]){
        float r,ar,cr;
        r=5.0f;
        ar=3.14f*r*r;
        cr=2*3.14f*r;

     
        System.out.println("Area : "+ar);
        System.out.println("circumference : "+cr);
        
        System.out.printf("\nArea : %.2f",ar);
        System.out.printf("\ncircumference : %.2f",cr);

    }
    
}
