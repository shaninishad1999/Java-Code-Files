// print 1 to 100 wihtout using number

class PrintOnetoHundred {
	//functinon 1
	public static void isPrintOnetoHundred(int one, String str) {

		one = 'A' / 'A';
		str = "..........";
		for (int i = one; i <= str.length() * str.length(); i++) {
			System.out.println(i);
		}
	}
	// function 2

	public static void isNewPrintOnetoHundred(int one) {

		one = 'a' / 'a';

		for (int i = one; i <= 'd'; i++) {
			System.out.println(i);
		}
	}

	public static void main(String args[]) {
		int one = 'A' / 'A';
		String str = "...........";
		isPrintOnetoHundred(one, str);
		isNewPrintOnetoHundred(one);
	}
}
